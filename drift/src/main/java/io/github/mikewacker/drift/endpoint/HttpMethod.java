package io.github.mikewacker.drift.endpoint;

/** Supported HTTP methods. */
public enum HttpMethod {

    /** HTTP GET method. */
    GET,

    /** HTTP PUT method. */
    PUT,

    /** HTTP POST method. */
    POST,

    /** HTTP DELETE method. */
    DELETE,

    /** HTTP PATCH method. */
    PATCH,

    /** HTTP HEAD method. */
    HEAD,
}
